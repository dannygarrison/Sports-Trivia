function normalize(s) {
  return s.toLowerCase()
    .replace(/university of /g,"").replace(/\buniversity\b/g,"")
    .replace(/\bcollege\b/g,"").replace(/\bstate\b/g,"st")
    .replace(/[^a-z0-9]/g,"").trim();
}
